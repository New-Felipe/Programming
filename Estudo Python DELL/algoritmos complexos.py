#Volume de um paralelepipedo:
base = float(input("Digite o valor da base do paralelepipedo: "))
altura = float(input("Digite o valor da altura do paralelepipedo: "))
largura = float(input("Digite o valor da largura do paralelepipedo: "))
volume = base * altura * largura

print("O volume do paralelepipedo é: ")
print(volume)

